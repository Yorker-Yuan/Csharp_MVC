        public override string[] GetRolesForUser(string account)
        {
            using (var context = new dbCustomerEntities())
            {   //多重權限
                var result = (from user in context.Member
                              join role in context.UserRole on user.fMemberId equals role.fid
                             where user.fAccount == account
                             select role.fRole).ToArray();
                return result;
            }
        }

